DROP TABLE IF EXISTS `MEMBER`;

CREATE TABLE `MEMBER` (
	`MEM_NO`	NUMBER	NOT NULL	COMMENT '회원번호',
	`MEM_ID`	VARCHAR2(16)	NOT NULL	COMMENT '회원아이디',
	`MEM_PWD`	VARCHAR2(16)	NOT NULL	COMMENT '회원비밀번호',
	`MEM_NAME`	VARCHAR2(10)	NOT NULL	COMMENT '회원명',
	`ADDRESS`	VARCHAR2(100)	NOT NULL	COMMENT '회원주소',
	`ADDRESS_DETAIL`	VARCHAR2(50)	NOT NULL	COMMENT '상세주소',
	`PHONE`	VARCHAR2(18)	NOT NULL	COMMENT '회원전화번호',
	`EMAIL`	VARCHAR2(50)	NOT NULL	COMMENT '회원이메일',
	`BIRTH_DATE`	DATE	NOT NULL	COMMENT '회원생년월일',
	`GENDER`	CHAR(2)	NOT NULL	COMMENT '회원성별',
	`GRADE`	VARCHAR2(10)	NOT NULL	DEFAULT '작은산(미정)'	COMMENT '회원등급',
	`ENROLL_DATE`	DATE	NOT NULL	DEFAULT SYSDATE	COMMENT '회원가입일',
	`STOP_YN`	CHAR(2)	NOT NULL	DEFAULT 'N'	COMMENT '회원정지여부',
	`STOP_DATE`	DATE	NULL	COMMENT '회원정지날짜',
	`DROP_YN`	CHAR(2)	NOT NULL	DEFAULT 'N'	COMMENT '회원탈퇴여부',
	`DROP_DATE`	DATE	NULL	DEFAULT SYSDATE	COMMENT '회원탈퇴일',
	`REP_COUNT`	NUMBER	NULL	DEFAULT 0	COMMENT '신고당한횟수'
);

DROP TABLE IF EXISTS `BOARD`;

CREATE TABLE `BOARD` (
