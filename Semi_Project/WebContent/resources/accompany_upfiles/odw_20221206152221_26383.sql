	`BOARD_ANSWER_YN`	CHAR(2)	NULL	DEFAULT 'N'	COMMENT '답변대기 - 'N' / 답변등록 - 'Y'',
	`LIKE_COUNT`	NUMBER	NOT NULL	DEFAULT 0
