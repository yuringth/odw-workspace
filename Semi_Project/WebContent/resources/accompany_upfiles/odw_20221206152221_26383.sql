	`BOARD_HT`	VARCHAR2(20)	NULL	COMMENT '말머리',
	`BOARD_TITLE`	VARCHAR2(50)	NOT NULL	COMMENT '글제목',
	`BOARD_CONTENT`	VARCHAR2(1000)	NULL	COMMENT '글내용',
	`BOARD_FILE`	VARCHAR2(500)	NULL	COMMENT '첨부파일의 URL',
	`BOARD_THUMBNAIL`	VARCHAR2(500)	NULL	COMMENT '썸네일의 URL',
	`BOARD_CREATE_DATE`	DATE	NOT NULL	DEFAULT SYSDATE	COMMENT '작성일',
	`BOARD_VIEW_COUNT`	NUMBER	NOT NULL	COMMENT '조회수',
	`BOARD_REPORT_STATUS`	VARCHAR2(5)	NOT NULL	COMMENT '신고여부',
	`BOARD_DELETE_STATUS`	VARCHAR(5)	NOT NULL	COMMENT '삭제여부',
	`METERIAL`	VARCHAR2(50)	NULL	COMMENT '준비물',
	`KEYWORD`	VARCHAR2(40)	NULL	COMMENT '키워드(해시태그)',
	`DP_DATE`	DATE	NULL	COMMENT '출발날짜',
